// Calculator class

public class Calculator {

	public int add(int x, int y) {
		return x + y;
	}

	public int subtract(int x, int y) {
		return x - y;
	}

	public int multiply(int x, int y) {
		return x * y;
	}

	public double divide(int x, int y) {
		return (double)x / (double)y;
	}

	public int modulus(int x, int y) {
		return x % y;
	}
	
}
